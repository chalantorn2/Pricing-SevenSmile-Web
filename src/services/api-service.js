// Autocomplete service
export const autocompleteService = {
  // Get autocomplete suggestions
  async getSuggestions(type, query) {
    if (!query || query.length < 2) {
      return [];
    }

    try {
      console.log(`üîç Fetching autocomplete for ${type}:`, query);
      const response = await apiCall(
        `/autocomplete.php?type=${encodeURIComponent(
          type
        )}&query=${encodeURIComponent(query)}`
      );
      console.log("‚úÖ Autocomplete results:", response.data?.length, "items");
      return response.data || [];
    } catch (error) {
      console.error("‚ùå Failed to fetch autocomplete:", error);
      return []; // Return empty array on error, don't throw
    }
  },
};
