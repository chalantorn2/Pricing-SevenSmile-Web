const API_BASE_URL = import.meta.env.VITE_API_BASE_URL || "/api";

// Helper function for API calls
async function apiCall(endpoint, options = {}) {
  const url = `${API_BASE_URL}${endpoint}`;
  const config = {
    headers: {
      "Content-Type": "application/json",
    },
    ...options,
  };

  console.log("üîó API Call:", url);
  console.log("üìù Config:", config);

  try {
    const response = await fetch(url, config);
    console.log("üì° Response Status:", response.status);
    console.log("üì° Response OK:", response.ok);

    if (!response.ok) {
      const data = await response.json();
      console.error("‚ùå HTTP Error:", response.status, data);
      throw new Error(data.error || `HTTP error! status: ${response.status}`);
    }

    // ‚úÖ ‡πÄ‡∏û‡∏¥‡πà‡∏° fallback ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö PHP Warning
    const text = await response.text();

    // ‡∏•‡∏≠‡∏á‡πÅ‡∏¢‡∏Å JSON ‡∏à‡∏≤‡∏Å PHP Warning
    let jsonText = text;
    if (text.includes('{"')) {
      // ‡∏´‡∏≤ JSON ‡∏™‡πà‡∏ß‡∏ô‡πÅ‡∏£‡∏Å‡πÉ‡∏ô‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°
      const jsonStart = text.indexOf('{"');
      if (jsonStart !== -1) {
        jsonText = text.substring(jsonStart);
      }
    }

    let data;
    try {
      data = JSON.parse(jsonText);
    } catch (parseError) {
      console.error(
        "‚ùå JSON Parse Error. Raw response:",
        text.substring(0, 500)
      );

      // ‚úÖ ‡∏ñ‡πâ‡∏≤ parse ‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡πÅ‡∏ï‡πà status 200 = success
      if (response.status === 200) {
        console.log("‚ö†Ô∏è Assuming success despite parse error");
        return { success: true, data: null };
      }

      throw new Error("Invalid JSON response from server");
    }

    console.log("üìä Response Data:", data);

    if (data.success === false) {
      console.error("‚ùå API Error:", data.error);
      throw new Error(data.error || "API Error");
    }

    return data;
  } catch (error) {
    console.error("üí• API Call Failed:", error);
    throw error;
  }
}

// Authentication functions (unchanged)
export const authService = {
  // Login
  async login(username, password) {
    try {
      console.log("üîê Attempting login for:", username);
      const response = await apiCall("/auth.php", {
        method: "POST",
        body: JSON.stringify({ username, password }),
      });

      // ‡πÄ‡∏Å‡πá‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• user ‡πÉ‡∏ô localStorage
      const userData = {
        id: response.data.id,
        username: response.data.username,
        role: response.data.role,
      };

      localStorage.setItem("user", JSON.stringify(userData));
      console.log("‚úÖ Login successful:", userData);
      return userData;
    } catch (error) {
      console.error("‚ùå Login failed:", error);
      throw error;
    }
  },

  // Logout
  logout() {
    localStorage.removeItem("user");
    console.log("üëã User logged out");
  },

  // Get current user
  getCurrentUser() {
    const user = localStorage.getItem("user");
    const userData = user ? JSON.parse(user) : null;
    console.log("üë§ Current user:", userData);
    return userData;
  },

  // Check if user is admin
  isAdmin() {
    const user = this.getCurrentUser();
    const isAdminUser = user && user.role === "admin";
    console.log("üîë Is admin:", isAdminUser);
    return isAdminUser;
  },
};

